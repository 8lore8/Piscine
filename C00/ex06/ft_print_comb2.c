#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a[4];
	int		i;
	int		j;

	i = -1;
	while (++i <= 98)
	{
		j = i;
		while (++j <= 99)
		{
			a[0] = i / 10 + '0';
			a[1] = i % 10 + '0';
			a[2] = j / 10 + '0';
			a[3] = j % 10 + '0';
			write (1, &a[0], 1);
			write (1, &a[1], 1);
			write (1, " ", 1);
			write (1, &a[2], 1);
			write (1, &a[3], 1);
			if (i != 98)
				write (1, ", ", 2);
		}
	}
}
