#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	j;

	j = 1;
	i = 0;
	if (str[i] == '\0')
		return (j);
	while (str[i] != '\0')
	{
		if (str[i] <= 48 || str[i] >= 57)
		{
			j = 0;
		}
		i++;
	}
	return (j);
}
